function onBeatHit()

   if curBeat == 60 then
      setProperty('defaultCamZoom', 1.1);
   end

   if curBeat == 64 then
      setProperty('defaultCamZoom', 0.5);
   end

   if curBeat == 128 then
      setProperty('defaultCamZoom', 0.9);
   end

   if curBeat == 192 then
      setProperty('defaultCamZoom', 0.6);
   end

   if curBeat == 256 then
      setProperty('defaultCamZoom', 0.7);
   end

   if curBeat == 316 then
      setProperty('defaultCamZoom', 0.8);
   end

   if curBeat == 320 then
      setProperty('defaultCamZoom', 0.6);
   end

   if curBeat == 384 then
      setProperty('defaultCamZoom', 0.5);
   end

   if curBeat == 480 then
      setProperty('defaultCamZoom', 0.7);
   end

   if curBeat == 512 then
      setProperty('defaultCamZoom', 0.5);
   end

end