function onBeatHit()

   if curBeat == 124 then
      setProperty('defaultCamZoom', 1.1);
   end

   if curBeat == 128 then
      setProperty('defaultCamZoom', 0.7);
   end

   if curBeat == 192 then
      setProperty('defaultCamZoom', 0.9);
   end

   if curBeat == 224 then
      setProperty('defaultCamZoom', 0.5);
   end

   if curBeat == 288 then
      setProperty('defaultCamZoom', 0.7);
   end

   if curBeat == 352 then
      setProperty('defaultCamZoom', 0.8);
   end

   if curBeat == 408 then
      setProperty('defaultCamZoom', 0.9);
   end

   if curBeat == 412 then
      setProperty('defaultCamZoom', 1.0);
   end

   if curBeat == 414 then
      setProperty('defaultCamZoom', 1.1);
   end

   if curBeat == 416 then
      setProperty('defaultCamZoom', 0.7);
   end

   if curBeat == 445 then
      setProperty('defaultCamZoom', 0.5);
   end

end