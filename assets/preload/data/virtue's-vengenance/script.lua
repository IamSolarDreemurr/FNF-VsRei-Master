function onBeatHit()

   if curBeat == 30 then
      setProperty('defaultCamZoom', 1.1);
   end

   if curBeat == 32 then
      setProperty('defaultCamZoom', 0.5);
   end

   if curBeat == 192 then
      setProperty('defaultCamZoom', 0.9);
   end

   if curBeat == 320 then
      setProperty('defaultCamZoom', 0.7);
   end

   if curBeat == 352 then
      setProperty('defaultCamZoom', 0.5);
   end

end