function onBeatHit()

   if curBeat == 64 then
      setProperty('defaultCamZoom', 0.7);
   end

   if curBeat == 128 then
      setProperty('defaultCamZoom', 0.5);
   end

   if curBeat == 192 then
      setProperty('defaultCamZoom', 0.7);
   end

   if curBeat == 224 then
      setProperty('defaultCamZoom', 0.7);
   end

   if curBeat == 256 then
      setProperty('defaultCamZoom', 0.6);
   end

   if curBeat == 288 then
      setProperty('defaultCamZoom', 0.7);
   end

   if curBeat == 320 then
      setProperty('defaultCamZoom', 0.6);
   end

   if curBeat == 384 then
      setProperty('defaultCamZoom', 0.7);
   end

   if curBeat == 416 then
      setProperty('defaultCamZoom', 0.8);
   end

   if curBeat == 432 then
      setProperty('defaultCamZoom', 0.6);
   end

   if curBeat == 448 then
      setProperty('defaultCamZoom', 1.0);
   end

   if curBeat == 512 then
      setProperty('defaultCamZoom', 0.7);
   end

   if curBeat == 540 then
      setProperty('defaultCamZoom', 0.8);
   end

   if curBeat == 544 then
      setProperty('defaultCamZoom', 0.6);
   end

   if curBeat == 576 then
      setProperty('defaultCamZoom', 0.7);
   end

   if curBeat == 640 then
      setProperty('defaultCamZoom', 0.5);
   end

end